class RelationalDemo{
      public static void main(String args[]){

           int a=10;
           int b=20;
 
           System.out.println("Is a>b:"+(a>b));
           System.out.println("Is a<b:"+(a<b));
           System.out.println("Is a>=b:"+(a>=b));
           System.out.println("Is a<=b:"+(a<=b));
           System.out.println("Is a==b:"+(a==b));
           System.out.println("Is a!=b:"+(a!=b));


 
    }
}