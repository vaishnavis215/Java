// to print even odd number between 1 to 50

class EvenOdd{
     
   public static void main(String args[])
   {
     
    
   for(int i=0;i<20;i++)
   { 
    if(i%2==0)
      {
        System.out.println("Even:"+i);
      }
   }
    for(int i=0;i<20;i++)
   { 
    if(i%2!=0)
      {
        System.out.println("odd:"+i);
      }
   }

   }

}